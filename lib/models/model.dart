class ModelForMovies {
  final String title;
  final String image;
  final String type;
  final String imdbRating;
  final String timeDuration;
  final String director;
  final String writer;
  final String desc;

  ModelForMovies({
    required this.title,
    required this.image,
    required this.type,
    required this.imdbRating,
    required this.timeDuration,
    required this.director,
    required this.writer,
    required this.desc,
  });
}
